    public function store(Request $request)
    {
        if (!Auth::check()) {
            // Verifica si el usuario NO está autenticado
            return redirect()->route('/')->withErrors('Debe iniciar sesión.');
        }
        $user = Auth::user();

        $request->validate([
            'pais_origen' => ['required', 'string', 'max:50', 'min:3'], 
            'nombre' => ['required', 'string', 'max:50', 'min:3'],
        ], $this->messages);

        $nuevo = NacionalidadModel::create([
            'pais_origen' => $request->pais_origen, 
            'nombre' => $request->nombre,
        ]);

        return redirect()->back()->with('success', ':) nacionalidad creado exitosamente.');
    }

    public function down(Request $request, $_id)
    {
        if (!Auth::check()) {
            // Verifica si el usuario NO está autenticado
            return redirect()->route('/')->withErrors('Debe iniciar sesión.');
        }
        $user = Auth::user();

        $buscado = NacionalidadModel::find($_id);

        if ($buscado->activo == 1) {
            $buscado->activo = 0;
            $buscado->save();
            return redirect()->back()->with('success', ':) nacionalidad apagado exitosamente.');
        }
        return redirect()->back()->withErrors('No se realizaron Cambios.');
    }
